/*
 * Operador de incremento y decremento
 * Aumenta (++) o disminuye (--) el numero en 1
 */

package com.mycompany.opincrementodecremento;

/**
 *
 * @author matthenao
 */
public class App {

    public static void main(String[] args) {
        int x = 5;
        
        x++; // Equivalente a decir x += 1 
        // Si el ++ se pone antes , incrementara primero y despues realizara la linea de codigo
        
        System.out.println(x);
    }
}
